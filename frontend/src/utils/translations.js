export function t(key, selectedLang) {
  const val = translations[key];
  if (typeof val === 'object' && val !== null) {
    return val[selectedLang] || val.en || key;
  }
  return val || key;
}

export function getQuestionLabel(q, selectedLang) {
  const labelObj = q.label;
  if (typeof labelObj === 'object' && labelObj !== null) {
    return labelObj[selectedLang] || labelObj.en || labelObj;
  }
  return labelObj;
}

export function getQuestionOptions(q, selectedLang) {
  const opts = q.options;
  if (typeof opts === 'object' && opts !== null && !Array.isArray(opts)) {
    return opts[selectedLang] || opts.en || [];
  }
  return Array.isArray(opts) ? opts : [];
}

export function getQuestionLabelWithDoctorName(label, doctorName) {
  return label.replace(/\{doctor_name\}/gi, doctorName);
}

export function getDoctorQuestionKey(doctorId, questionId) {
  return `doctor_${doctorId}_${questionId}`;
}
